names = ["Jake", "Zac", "Ian", "Ron", "Sam", "Dave"]   # list of names

search_name = input("Enter name to Find: ")  # user input to search name from the list

found = False    # assuming that the name is not found in the list

for name in names:  # Checking the names one by one in the list
    if name == search_name:    # matching the name from the user input and from the list
        found = True
        break     # to finish the code if found

if found:
    print("Found the name")
else:
    print("No maching Found")
