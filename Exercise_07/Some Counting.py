# Counting numbers from 0 to 50 and increments of 1
for i in range(0,50,1):
  print(i)

# Counting numbers from 50 to 0 and decrements of 1
for i in range(50,0,-1):
  print(i)

# Counting numbers from 30 to 50 and increments of 1
for i in range(30,50,1):
  print(i)

# Counting numbers from 50 to 10 and decrements of 2
for i in range(50,10,-2):
  print(i)

